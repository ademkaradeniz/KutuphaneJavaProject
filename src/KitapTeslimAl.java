    private void btnAraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAraActionPerformed
        cmbxBarkodNo.removeAllItems();
        String sql = "Select * from EmanetKitaplar where OgrenciTCNo=? ";
        try {
            preparedStatement = connection.prepareStatement(sql);
            preparedStatement.setString(1, txtOgrTc.getText());
            resultSet = preparedStatement.executeQuery();
            while (resultSet.next()) {
                cmbxBarkodNo.addItem(resultSet.getString("KitapBarkodNo"));
            }
            ogrenciBilgiDoldur(txtOgrTc.getText());
            resultSet.close();
            preparedStatement.close();
        } catch (SQLException exception) {
            JOptionPane.showMessageDialog(null, exception);

        } finally {
            try {
                preparedStatement.close();
                resultSet.close();
            } catch (SQLException exception) {
                JOptionPane.showMessageDialog(null, exception);
            }
        }

    }//GEN-LAST:event_btnAraActionPerformed

    private void cmbxBarkodNoİtemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbxBarkodNoİtemStateChanged
        // TODO add your handling code here:

        ResultSet resultSetKitap;
        PreparedStatement preparedStatementKitap;
        String sql = "Select * from EmanetKitaplar where KitapBarkodNo=?";
        try {
            txtKitapClear();
            preparedStatementKitap = connection.prepareStatement(sql);
            preparedStatementKitap.setString(1, (String) cmbxBarkodNo.getSelectedItem());
            resultSetKitap = preparedStatementKitap.executeQuery();
            if (resultSetKitap.next()) {
                String ekle1 = resultSetKitap.getString("KitapAdi");
                txtKitapAdi.setText(ekle1);
                String ekle2 = resultSetKitap.getString("Yazar");
                txtYazar.setText(ekle2);
                String ekle3 = resultSetKitap.getString("Yayinevi");
                txtYayinevi.setText(ekle3);
                String ekle4 = resultSetKitap.getString("Tur");
                txtTur.setText(ekle4);
                String ekle5 = resultSetKitap.getString("Sayfa");
                txtSayfa.setText(ekle5);
                String ekle6 = resultSetKitap.getString("OduncTarih");
                txtOduncTarih.setText(ekle6);
                resultSetKitap.close();
                preparedStatementKitap.close();
            } else {
                resultSetKitap.close();
                preparedStatementKitap.close();
            }
        } catch (SQLException exception) {
            JOptionPane.showMessageDialog(null, exception);
        }
    }//GEN-LAST:event_cmbxBarkodNoİtemStateChanged

