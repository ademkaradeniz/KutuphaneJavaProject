    public void emanetGecmisineEkle() {
        String sql = "Insert into EmanetGecmis(KitapBarkodNo,OgrenciTCNo,OduncTarih,DonusTarih,Status,CreateOrModifyDate)values(?,?,?,?,?,?)";
        try {

            preparedStatement = connection.prepareStatement(sql);
            preparedStatement.setString(1, cmbxBarkodNo.getSelectedItem().toString());
            preparedStatement.setString(2, txtOgrTc.getText());
            preparedStatement.setString(3, txtOduncTarih.getText());
            SimpleDateFormat formatter = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            preparedStatement.setString(4, formatter.format(txtDonusTarih.getDate()));
            preparedStatement.setString(5, "1");
            preparedStatement.setString(6, GeneralAppService.createNowDate().toString());
            preparedStatement.execute();

        } catch (SQLException exception) {
            JOptionPane.showMessageDialog(null, exception);

        }
    }

